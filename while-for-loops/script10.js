//loops-lecture, while loop
var age = 5;
while (age < 10) {
    console.log('your age is less than 10');
    age++;
} 
console.log('you are now over 10');

// while
let w = 0;
while (w < 5) {
  console.log(`This is while iteration number ${w}`);
  w++;
}

